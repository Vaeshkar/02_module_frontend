/* Arithmetics Exercises WBS JS Module 02 */

// Addition with Mixed Types
let numberOne = 47
let stringOne = "47"
console.log(numberOne + stringOne)
/* Explanation: JavaScript automatically coerces the number into a string. */

// Subtration with Mixed Types
console.log(numberOne - stringOne)
/* Explanation: Javascript performs normal numeric subtraction. */

// Multiplication with a String
let numberTwo = 74
console.log(stringOne + numberTwo)

// Division by a String
console.log(numberTwo / stringOne)

// Modulus Operation
console.log(numberOne % numberTwo)
numberTwo = "36"
console.log(numberOne % numberTwo)

// Perform a Series of Numeric Operations

// Define new numeric variables
let a = 10;
let b = 5;
let c = 2;

// Perform and print the result of each operation

// Addition
console.log(a + b + c); // 10 + 5 + 2 = 17

// Subtraction
console.log(a - b - c); // 10 - 5 - 2 = 3

// Multiplication
console.log(a * b * c); // 10 * 5 * 2 = 100

// Division
console.log(a / b / c); // 10 / 5 / 2 = 1

// Modulus
console.log(a % b % c); // 10 % 5 = 0, then 0 % 2 = 0
