/* 1. Initialize an Array */
const myArray = [1,2,3,4,5]
console.log("Initialize an Array")
console.log(myArray)

/* 2. Add Elements to the End of the Array */
myArray.push(6, 7)
console.log("Add one item to the Array")
console.log(myArray)

/* 3. Remove the Last Element from the Array */
myArray.pop()
console.log("Remove the last element from the Array")
console.log(myArray)

/* 4. Remove the First Element from the Array */
myArray.shift()
console.log("Remove the first element from the Array")
console.log(myArray)

/* 5. Add Eelements to the Beginning of the Array */
myArray.unshift(0, 1)
console.log("Add elements to the beginning of the Array")
console.log(myArray)